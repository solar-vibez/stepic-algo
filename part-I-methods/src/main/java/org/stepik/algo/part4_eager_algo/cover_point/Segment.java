package org.stepik.algo.part4_eager_algo.cover_point;

class Segment {
    private Integer begin;
    private Integer end;

    Segment(Integer begin, Integer end) {
        this.begin = begin;
        this.end = end;
    }

    Integer getBegin() {
        return begin;
    }


    Integer getEnd() {
        return end;
    }

}
